package sentenzaDoWhile;

public class SentenzaDoWhile {
	public static void main(String[]args) {
		int cont = 0;
// Para poñer varias liñas como comentario, señeccionamos as liñas que queiramos e pulsamos "Ctrl + 7"

	// SENTENZA DO WHILE (REPETIR)
		do {
			cont=cont+1;
			System.out.println(cont);
		}while(cont<10);
		System.out.println("\n\t Final do DO While");
		}
}
