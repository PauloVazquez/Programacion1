/* As variables booleanas só poden ter 2 valores
 * Verdadeiro ou falso
 * Pode usarse a variable booleana sen ter que comparala
 */

package variablesBooleanas;

public class VariablesBooleanas {
	public static void main(String[]args) {
		boolean existe = false;
		// if existe implica que existe é verdadeiro
		if (existe) {
			//proceso
			}else {
				//proceso			
		}
		// if !existe implica que existe é falso, sería negar a booleana
		if (!existe) {
			//proceso
			}else {
				//proceso			
		}
	}

}
